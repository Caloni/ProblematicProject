#include "stringFunctions.hpp"
#include <iostream>

using namespace std;


string TwoStringsToStringWithAndInTheMiddle(string leftString, string rightString)
{
	return leftString + " and " + rightString;
}


std::string ConcatenateTwoStrings(std::string firstString, std::string secondString)
{
	return firstString + secondString;
}
